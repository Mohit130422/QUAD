                    <h3 class="head-font">Second Quad Leaders’ Summit (first in-person)</h3>
                    <p class="date">24 September 2021</p>
                    <img src="assets/images/second_meet.jpeg" alt="">
                    <p>The second Quad Leaders’ Summit (first in-person) was held on 24 September 2021 in Washington DC. The Leaders announced three new Working Groups on Infrastructure, Space, and Cyber issues. 100 STEM fellowships (25 from each Quad country) to US universities for graduate and doctoral students from Quad countries were announced.</p>
