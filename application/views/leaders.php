                    <h3 class="head-font">First Quad Leaders’ Summit taking place in virtual format</h3>
                    <p class="date">12 March 2021</p>
                    <img src="assets/images/first_meet.jpg" alt="">
                    <p>Quad saw a natural evolution and elevation in 2021 to the level of the Leaders with the first Quad Leaders’ Summit taking place in virtual format on 12 March 2021. Leaders discussed regional and global issues, especially the Indo-Pacific. Three Working Groups were announced by the Leaders - Vaccine Partnership, Climate Change, and Critical & Emerging Technologies. The Quad adopted an action-oriented approach to enable practical cooperation.</p>
