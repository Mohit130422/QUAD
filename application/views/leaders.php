                    <h3 class="head-font">Fourth Quad Leaders’ Summit (second in-person) was hosted by Japan</h3>
                    <p class="date">24 May 2022</p>
                    <img src="assets/images/fourth_meet.jpg" alt="">
                    <p>Leaders launched the Quad Satellite Data Portal to provide regional countries with space-based data for climate and disaster forecasting and sustainable use of marine resources; Q-CHAMP for practical cooperation on climate action, climate finance, and technology transfer to help countries meet their climate commitments; Quad Debt Management Resource Portal to address challenges of unsustainable debt financing; Quad Partnership on HADR to provide timely and effective assistance in the Indo-Pacific region in the event of future incidents.</p>
